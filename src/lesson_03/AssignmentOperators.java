package lesson_03;

public class AssignmentOperators {
    public static void main(String[] args) {

        //Optional declaration of variable
        var a = 30;
        System.out.println("a = " + a);
        a = 50;

        System.out.println("a = " + a);

//        a = a + 5;
        a += 5;
        System.out.println("a = " + a);

        a -= 10;
        System.out.println("a = " + a);

        a *= 2;
        System.out.println("a = " + a);

        a /= 2;
        System.out.println("a = " + a);

        a %= 6;
        System.out.println("a = " + a);







    }
}
